  - 整数同士の割り算は整数となる（小数点以下は切り捨てられる）。小数点以下が必要なら、どちらかの値に小数点.0を付ける
  - 少数を扱う場合は、丸め誤差が起きるので、気を付けておく
  - falseとnil以外はすべて真（数値、文字列も）
  - data = find_data
