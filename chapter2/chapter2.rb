- &&, ||は短絡評価される。左から順に式を評価し、式全体の真偽が確定すると、評価を終了する。1 && nilの時点でnilが確定している
  1 && nil && 3 #=> nil 
- 「if文で否定の条件」を書いているときは、unlessが使える。読みやすい方を使う
- elsifを重ねる場合は、case文が使える
  country = 'Italy'

  case country
  when 'Japan'
    'こんにちは'
  wehn 'Italy'
    'CIao'
  else
    '?'
  end

  #=> "Ciao"

- ifは三項演算子に変換できる
  n = 11
  n > 10 ? '10より大きい' : '10以下'
  #=> "10より大きい"
